package com.gestionestudiantil.model;

import java.io.Serializable;

import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author Tex
 */
@Entity
@Table(name = "rol_usuario")
@NamedQueries({ @NamedQuery(name = "RolUsuario.findAll", query = "SELECT r FROM RolUsuario r") })
public class RolUsuario implements Serializable {
	private static final long serialVersionUID = 1L;
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Basic(optional = false)
	@Column(name = "id", nullable = false)
	private Long id;
	@JoinColumn(name = "rol_id", referencedColumnName = "id", nullable = false)
	@ManyToOne(optional = false)
	private Rol rol;
	@JoinColumn(name = "usuario_id", referencedColumnName = "id", nullable = false)
	@ManyToOne(optional = false)
	private Usuario usuario;

	public RolUsuario() {
	}

	public RolUsuario(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Rol getRolId() {
		return rol;
	}

	public void setRol(Rol rol) {
		this.rol = rol;
	}

	public Usuario getUsuario() {
		return usuario;
	}

	public void setUsuario(Usuario usuario) {
		this.usuario = usuario;
	}

	public Rol getRol() {
		return rol;
	}

	@Override
	public int hashCode() {
		int hash = 0;
		hash += (id != null ? id.hashCode() : 0);
		return hash;
	}

	@Override
	public boolean equals(Object object) {
		if (!(object instanceof RolUsuario)) {
			return false;
		}
		RolUsuario other = (RolUsuario) object;
		if ((this.id == null && other.id != null)
				|| (this.id != null && !this.id.equals(other.id))) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "com.gestionestudiantil.model.RolUsuario[ id=" + id + " ]";
	}

}
